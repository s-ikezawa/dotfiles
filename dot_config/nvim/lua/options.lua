-- ============================================================================
-- Neovim オプション設定
-- ============================================================================

local opt = vim.opt -- グローバル/ウィンドウ/バッファオプション用のショートハンド
local g = vim.g     -- グローバル変数用のショートハンド

-- ----------------------------------------------------------------------------
-- リーダーキー
-- ----------------------------------------------------------------------------
g.mapleader = ","      -- リーダーキーを "," に設定（プラグイン読み込み前に設定する必要がある）
g.maplocalleader = "," -- ローカルリーダーキーも "," に設定

-- ----------------------------------------------------------------------------
-- 言語プロバイダーの無効化
-- 外部言語の remote plugin を使わないため全プロバイダーを無効化する
-- （起動の高速化と :checkhealth の警告抑制）
-- ----------------------------------------------------------------------------
g.loaded_python3_provider = 0 -- Python 3 プロバイダーを無効化
g.loaded_ruby_provider = 0    -- Ruby プロバイダーを無効化
g.loaded_perl_provider = 0    -- Perl プロバイダーを無効化
g.loaded_node_provider = 0    -- Node.js プロバイダーを無効化

-- ----------------------------------------------------------------------------
-- 表示・外観
-- ----------------------------------------------------------------------------
opt.number = true         -- 行番号を表示する
opt.relativenumber = true -- 現在行からの相対行番号を表示する（移動が楽になる）
opt.cursorline = true     -- カーソル行をハイライトする
opt.termguicolors = true  -- 24bit カラー（True Color）を有効にする
opt.signcolumn = "yes"    -- 記号列を常に表示してテキストのズレを防ぐ
opt.colorcolumn = ""      -- 縦線（カラーカラム）を表示しない
opt.wrap = false          -- 長い行を折り返さずに表示する
opt.scrolloff = 8         -- カーソルの上下に最低 8 行のスペースを確保する
opt.sidescrolloff = 8     -- カーソルの左右に最低 8 桁のスペースを確保する
opt.showmode = false      -- モード表示（-- INSERT -- など）を非表示にする（ステータスラインに任せる）
-- コマンドライン行は最終的に非表示(0)にする（cmdline/メッセージは noice.nvim が描画）。
-- ただし起動直後から 0 にすると、modes.nvim が mode ハイライト更新のため
-- vim.schedule で呼ぶ空の nvim_echo が hit-enter プロンプト（"Press any key to continue"）
-- を誘発する（Neovim 既知の挙動: cmdheight=0 × scheduled nvim_echo, neovim#22875）。
-- 起動シーケンス完了後に 0 へ落とすことで、起動時 echo を cmdheight=1 の状態で消化させる。
opt.cmdheight = 1
vim.api.nvim_create_autocmd("UIEnter", {
  once = true,
  callback = function()
    vim.defer_fn(function() vim.o.cmdheight = 0 end, 200)
  end,
})
opt.pumheight = 10        -- 補完ポップアップに表示する最大項目数
opt.laststatus = 0        -- ステータスラインを非表示にする（ファイル名は incline.nvim が各ウィンドウ右上にフロート表示）
opt.showtabline = 0       -- タブラインを表示しない
opt.list = true           -- 不可視文字（タブ・行末スペースなど）を表示する
opt.listchars = { tab = "» ", trail = "·", nbsp = "␣" } -- 不可視文字の表示方法を指定する

-- ----------------------------------------------------------------------------
-- インデント・タブ
-- ----------------------------------------------------------------------------
opt.expandtab = true   -- タブの代わりにスペースを挿入する
opt.tabstop = 2        -- 画面上のタブ幅を 2 にする
opt.softtabstop = 2    -- 編集時にタブとして扱うスペース数を 2 にする
opt.shiftwidth = 2     -- 自動インデント / >> << のインデント幅を 2 にする
opt.smartindent = true -- 言語に応じた賢い自動インデントを有効にする
opt.autoindent = true  -- 改行時に前の行のインデントを引き継ぐ
opt.breakindent = true -- 折り返した行のインデントを揃える

-- ----------------------------------------------------------------------------
-- 検索
-- ----------------------------------------------------------------------------
opt.ignorecase = true -- 検索時に大文字小文字を区別しない
opt.smartcase = true  -- 検索語に大文字が含まれる場合のみ大文字小文字を区別する
opt.hlsearch = true   -- 検索結果をハイライトする
opt.incsearch = true  -- 検索文字の入力中にマッチ箇所をインクリメンタル表示する

-- ----------------------------------------------------------------------------
-- 編集・操作
-- ----------------------------------------------------------------------------
opt.mouse = "a"                             -- すべてのモードでマウス操作を有効にする
opt.clipboard = "unnamedplus"               -- システムクリップボードと yank/paste を共有する
opt.undofile = true                         -- アンドゥ履歴をファイルに保存して再起動後も保持する
opt.virtualedit = "block"                   -- 矩形選択時にテキストのない位置にもカーソルを置ける
opt.completeopt = { "menuone", "noselect" } -- 補完を 1 件でもメニュー表示し、自動選択しない
opt.inccommand = "split"                    -- :substitute の置換結果をプレビュー表示する
opt.iskeyword:append("-")                   -- ハイフン区切りの語を 1 単語として扱う
opt.confirm = true                          -- 保存していない変更がある場合に確認ダイアログを出す

-- ----------------------------------------------------------------------------
-- ファイル・エンコーディング・バックアップ
-- ----------------------------------------------------------------------------
opt.encoding = "utf-8"     -- 内部で扱う文字エンコーディングを UTF-8 にする
opt.fileencoding = "utf-8" -- ファイル書き込み時のエンコーディングを UTF-8 にする
opt.swapfile = false       -- スワップファイルを作成しない
opt.backup = false         -- バックアップファイルを作成しない
opt.writebackup = false    -- 上書き前のバックアップを作成しない
opt.autoread = true        -- 外部で変更されたファイルを自動的に読み直す

-- ----------------------------------------------------------------------------
-- ウィンドウ・分割
-- ----------------------------------------------------------------------------
opt.splitright = true -- 縦分割時に新しいウィンドウを右に開く
opt.splitbelow = true -- 横分割時に新しいウィンドウを下に開く

-- ----------------------------------------------------------------------------
-- パフォーマンス・タイミング
-- ----------------------------------------------------------------------------
opt.updatetime = 250   -- スワップ書き込み / CursorHold までの待ち時間（ms）を短くする
opt.timeoutlen = 300   -- マッピングのキー入力待ち時間（ms）
opt.lazyredraw = false -- マクロ実行中の再描画を抑制しない（最近は false 推奨）
opt.synmaxcol = 300    -- シンタックスハイライトを行う最大桁数（長い行の速度対策）

-- ----------------------------------------------------------------------------
-- 折りたたみ（Folding）
-- ----------------------------------------------------------------------------
opt.foldmethod = "expr"                     -- 折りたたみの方法に式を使う
opt.foldexpr = "nvim_treesitter#foldexpr()" -- Treesitter による折りたたみを使う
opt.foldlevel = 99                          -- 起動時はすべての折りたたみを開いておく
opt.foldlevelstart = 99                     -- ファイルを開いた直後の折りたたみレベル
opt.foldenable = true                       -- 折りたたみ機能を有効にする
