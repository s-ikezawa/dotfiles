-- ~/.config/nvim/lua/options.lua
-- 基本的なエディタ設定

local opt = vim.opt

-- ============================================================
-- 表示・外観 (Appearance)
-- ============================================================
opt.number = true            -- 行番号を表示する
opt.relativenumber = true    -- カーソル行からの相対行番号を表示する
opt.cursorline = true        -- カーソルのある行をハイライトする
opt.signcolumn = "yes"       -- サインカラムを常に表示する(表示ガタつき防止)
-- opt.colorcolumn = "80"       -- 80桁目に縦線を表示する
opt.wrap = false             -- 長い行を折り返さない
opt.termguicolors = true     -- True Color (24bitカラー) を有効にする
opt.laststatus = 0           -- ステータスラインを表示しない
opt.showmode = true          -- モードをコマンドラインに表示する(ステータスライン非表示のため)
opt.cmdheight = 0            -- 下部のコマンドライン行を表示しない(noice で中央入力)
opt.pumheight = 10           -- 補完候補ポップアップの最大表示数
opt.shortmess:append("W")    -- 保存時の "written" メッセージを抑制(notify で代替表示)

-- ============================================================
-- インデント・タブ (Indentation)
-- ============================================================
opt.expandtab = true         -- タブをスペースに展開する
opt.tabstop = 2              -- タブ文字の表示幅を2スペースにする
opt.shiftwidth = 2           -- 自動インデントの幅を2スペースにする
opt.softtabstop = 2          -- 編集時のタブ操作幅を2スペースにする
opt.autoindent = true        -- 改行時に前行のインデントを引き継ぐ
opt.smartindent = true       -- 構文に応じて賢くインデントする

-- ============================================================
-- 検索 (Search)
-- ============================================================
opt.ignorecase = true        -- 検索時に大文字小文字を区別しない
opt.smartcase = true         -- 検索語に大文字を含む場合は区別する
opt.hlsearch = true          -- 検索結果をハイライトする
opt.incsearch = true         -- 入力中にインクリメンタル検索する

-- ============================================================
-- 編集・操作 (Editing)
-- ============================================================
opt.clipboard = "unnamedplus" -- システムのクリップボードと連携する
opt.mouse = ""                -- 全モードでマウス操作を無効にする
opt.undofile = true          -- アンドゥ履歴をファイルに保存し再起動後も保持する
opt.backup = false           -- バックアップファイルを作成しない
opt.swapfile = false         -- スワップファイルを作成しない
opt.virtualedit = "block"    -- 矩形選択時に文字の無い位置へも移動できる

-- ============================================================
-- ウィンドウ・分割 (Window / Split)
-- ============================================================
opt.splitright = true        -- 縦分割時に新ウィンドウを右に開く
opt.splitbelow = true        -- 横分割時に新ウィンドウを下に開く
opt.scrolloff = 8            -- カーソル上下に最低8行の余白を確保する
opt.sidescrolloff = 8        -- カーソル左右に最低8桁の余白を確保する

-- ============================================================
-- 動作・パフォーマンス (Behavior)
-- ============================================================
opt.updatetime = 250         -- 更新間隔(ms)。短くしてレスポンスを良くする
opt.timeoutlen = 300         -- キーマッピング入力の待ち時間(ms)
opt.fileencoding = "utf-8"   -- ファイルの保存エンコーディングをUTF-8にする
opt.completeopt = { "menuone", "noselect" } -- 補完の挙動を調整する
