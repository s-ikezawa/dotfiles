-- =============================================================================
-- Neovim Options
-- =============================================================================
-- :help option-list で各オプションの詳細を確認可能
-- Neovim 0.10+ では vim.o がテーブル値もサポートするようになり、
-- スカラー値は vim.o、リストの追加/削除など操作が必要なものは vim.opt を使うのが
-- 最新の推奨スタイル
-- =============================================================================

-- ----- 行番号 ----------------------------------------------------------------
-- 現在行に絶対行番号、それ以外に相対行番号を表示する (ハイブリッド表示)
-- 相対行番号があると `5j` `10k` などの移動コマンドの距離が即座に分かる
vim.o.number = true
vim.o.relativenumber = true

-- ----- マウス ----------------------------------------------------------------
-- すべてのモード (normal/visual/insert/command) でマウス操作を有効化
vim.o.mouse = "a"

-- ----- モード表示 ------------------------------------------------------------
-- ステータスラインプラグイン (lualine等) が現在のモードを表示するため、
-- コマンドライン下部のデフォルトのモード表示 (-- INSERT --) は非表示にする
vim.o.showmode = false

-- ----- クリップボード --------------------------------------------------------
-- OS のクリップボードと yank/paste を共有する
-- vim.schedule で遅延させることで起動時間を短縮する (kickstart.nvim 由来)
vim.schedule(function()
  vim.o.clipboard = "unnamedplus"
end)

-- ----- インデント ------------------------------------------------------------
-- 折り返し行のインデントを揃えて見やすくする
vim.o.breakindent = true

-- 折り返さない (横スクロール)。長い行を折り返したい場合は true に
vim.o.wrap = false

-- 新しい行のインデントを賢く挿入する (C/Java系の構文に基づいた自動インデント)
vim.o.smartindent = true

-- Tab を空白に展開する
vim.o.expandtab = true

-- Tab 文字の表示幅
vim.o.tabstop = 2

-- 自動インデント (>>, <<, == など) の幅
vim.o.shiftwidth = 2

-- 編集中に Tab キーで挿入される空白の数 (-1 で shiftwidth と同じ値になる)
vim.o.softtabstop = -1

-- ----- 永続化 / バックアップ -------------------------------------------------
-- undo 履歴をファイルに保存し、Neovim を閉じても undo を可能にする
vim.o.undofile = true

-- スワップファイルを作成しない (編集中のクラッシュ復旧用ファイル)
-- undofile + git があれば実用上ほぼ不要で、ノイズを減らせる
vim.o.swapfile = false

-- バックアップファイルを作成しない
vim.o.backup = false

-- 既定の文字エンコーディング
vim.o.fileencoding = "utf-8"

-- ----- 検索 ------------------------------------------------------------------
-- 検索時に大文字小文字を区別しない
vim.o.ignorecase = true

-- ただし大文字を含む場合は区別する (ignorecase と組み合わせて使う)
vim.o.smartcase = true

-- 検索結果をハイライトする (:nohlsearch または <Esc> で解除)
vim.o.hlsearch = true

-- インクリメンタル検索 (入力中にマッチ箇所を順次ハイライト)
vim.o.incsearch = true

-- :substitute コマンドの結果を分割ウィンドウでライブプレビュー表示
vim.o.inccommand = "split"

-- ----- 表示 ------------------------------------------------------------------
-- サインカラム (LSP診断/Gitマーカーが出る左の列) を常時表示
-- "yes" にしないと表示の出現/消滅でテキストが横に揺れて読みづらい
vim.o.signcolumn = "yes"

-- カーソルがある行をハイライトする
vim.o.cursorline = true

-- スクロール時に上下に確保する最低行数 (中央付近にカーソルを保ちやすい)
vim.o.scrolloff = 10

-- 24bit カラー (TrueColor) を有効にする。多くのモダンなカラースキームに必須
vim.o.termguicolors = true

-- 不可視文字を表示する
vim.o.list = true
-- 表示する不可視文字の種類: tab, 行末スペース, 全角スペース等
vim.opt.listchars = { tab = "» ", trail = "·", nbsp = "␣" }

-- 補完メニュー (popup menu) の最大表示行数
vim.o.pumheight = 10

-- conceal 機能のレベル (markdown のリンク表示などで使用)
vim.o.conceallevel = 2

-- ----- ウィンドウ分割 --------------------------------------------------------
-- :split で下に、:vsplit で右に新しいウィンドウを開く (より直感的)
vim.o.splitbelow = true
vim.o.splitright = true

-- 分割/閉じた時に画面のテキスト位置を維持する (カーソル位置ではなく screen 位置基準)
-- 連携プラグイン:
--   - snacks.nvim (picker)  : プレビュー split を開閉した際にテキストが上下に揺れない
--   - sidekick.nvim (cli)   : CLI を split で開閉した時のジャンプを抑える
vim.o.splitkeep = "screen"

-- ----- 浮動ウィンドウ --------------------------------------------------------
-- 浮動ウィンドウのデフォルト枠線スタイル (Neovim 0.11+)。
-- nvim_open_win() で border を明示しない呼び出しすべてに適用されるグローバル既定値。
-- 連携プラグイン:
--   - snacks.nvim (picker)  : ピッカー本体・プレビュー・input の枠が丸枠で統一される
--   - sidekick.nvim (cli)   : 浮動表示モードの CLI ウィンドウの枠線
--   - 将来の LSP hover / signature help も同じ枠線になる
vim.o.winborder = "rounded"

-- ----- フォールド ------------------------------------------------------------
-- 折りたたみのアルゴリズムは Tree-sitter ベースを使う
-- (foldexpr/foldmethod は lua/plugins/treesitter.lua で FileType ごとに設定)
--
-- ファイルを開いた時に折りたたまれた状態になると視認性が落ちるため、
-- 初期 foldlevel を十分高くして「全展開された状態」で開始する。
-- 折りたたみたいときは zc (現 fold を閉じる) や zM (全閉じ) を使う。
vim.o.foldlevel = 99
vim.o.foldlevelstart = 99
-- foldenable=true のままにすれば fold 機能自体は使える (zc/zM などの操作が可能)
vim.o.foldenable = true

-- ----- 応答性 ----------------------------------------------------------------
-- スワップファイル書き込み/CursorHold イベント発火までの待機時間 (ms)
-- LSP やプラグインのレスポンスに影響するため短めにする
vim.o.updatetime = 250

-- キーマッピング待機時間 (ms)。複数キーのマッピング (例: <leader>al) で
-- 次のキーを待つ時間。短すぎるとタイプが追いつかず、長すぎると単発キーの
-- 反応が遅く感じる。Vim デフォルトは 1000ms、LazyVim は 300ms。
-- which-key 系プラグインのポップアップ表示タイミングにも影響する。
vim.o.timeoutlen = 500

-- ----- 操作性 ----------------------------------------------------------------
-- 未保存ファイルがある状態で終了しようとした際に確認ダイアログを表示
-- (E37 エラーで失敗する代わりに保存するか聞いてくれる)
vim.o.confirm = true
